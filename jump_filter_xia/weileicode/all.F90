    program main

    use com

    call MPI_INIT(ierr)
    call MPI_COMM_RANK(MPI_COMM_WORLD,myid,ierr)
    call MPI_COMM_SIZE(MPI_COMM_WORLD,numprocs,ierr)
    call CPU_TIME(t1)

    myid1 = myid + 1

    myidx = mod(myid1,Nx_process)
    if (myidx == 0) then
        myidx = Nx_process
    end if

    myidy = (myid1 - myidx)/Nx_process + 1

    print *,"process",myid1,"is alive,the index is",myidx,myidy

    call init_data

    if (RKorder == 1) then
    else if (RKorder == 3) then
        call RK3
    else if (RKorder == 4) then
        call RK4
    end if

    call set_bc

    call save_solution
    
    call  writetroubledcells
    
    call calculate_L2_Error

    call CPU_TIME(t2)

    if (myid1 == 1) then
        open(unit = 1,file = 'time.txt')
        write(1,*) t2 - t1
        print *,"Run time is",t2 - t1,"second"
        close(1)
    end if

    call MPI_FINALIZE(rc)

    end program main

